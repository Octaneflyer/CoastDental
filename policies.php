<strong>Child Safety</strong><strong><br>
<br>
</strong> The practice is committed to create and maintain a safe environment for children and young people. This practice recognises the complexity of laws regulating child minding and has created this policy to ensure that the staff members are not given the responsibility to look after the children of patients.<br>
<br>
Whilst on the practice premises, children and young people must be accompanied by an adult carer at all times. As the staff members are not registered child minders they are unable to accept the responsibility for looking after young children whilst their carer is having dental treatment.<br>
<br>
The reception staff will advise adult patients with carers&rsquo; responsibilities for young children to make alternative childcare arrangements whilst attending dental appointments whenever possible. If the arrangements cannot be made the reception staff will ask the carer to take the child into the treatment room with them. If a patient is unwilling to co-operate the reception staff must consult promptly with the Practice Manager. The full Child Protection Policy can be viewed in (M 295). <strong><br>
