Disability</strong><br>
<br>
All reasonable steps have been taken to ensure that premises are accessible to all those who need to use them in keeping with the requirements of the Disability Discrimination Act 1995. The practice is committed to complying with the Disability Discrimination Act 1995 and the Equality Act 2010 by ensuring that disabled patients have the same access to our services as non-disabled patients. For the purpose of this policy the term disabled may include people with physical and sensory impairments, with learning disabilities, chronic or terminal illness and users of mental health services. The application of all policies and procedures ensures that people are protected from unlawful discrimination. <br>
<br>
The practice management has made every effort with facilities, policies, procedures, communication, signage and staff training to ensure the ease of access to our services.  Practice information is available in alternative formats including large print, audiocassette, CD and in Braille. <br>
<br>
The practice staff familiarise themselves with the requirements of the Disability Discrimination Act and Equality Act and receive training in issues relevant and important to disabled people. The members of the practice strive to use language that is easy to understand and meets the needs of all disabled people. When communicating with disabled patients staff:
<ul>
  <li>Ask everyone about their requirements in advance &lsquo;Please let me know if you require any particular assistance&rsquo; and be able to respond sensibly</li>
  <li>Do not patronise, make assumptions or think they know best</li>
  <li>Are ready to offer assistance, but never impose it</li>
  <li>Are prepared as necessary to :</li>
  <li>Sit or bend down to talk to a person at his or her eye-level</li>
  <li>Offer a seat or help with doors</li>
  <li>Let the person take their arm for guidance or support</li>
  <li>Offer the use of equipment, e.g. a clipboard as an alternative writing surface</li>
  <li>Use appropriate ways of communicating, e.g. writing notes if someone finds speech difficult to understand</li>
  <li>Be courteous, patient and always talk to a disabled person directly, never through his or her companion; never shout or call attention to anyone; never compromise the person&rsquo;s right to privacy or confidentiality; check to make sure they have been understood</li>
</ul>
The practice management welcomes patients&rsquo; views and suggestions on how we can improve services. If suggestions are made the practice will inform everyone about the adjustment plans and their proposed completion dates. <strong><br>
