Child and Vulnerable Adult Protection<br>
<br>
</strong> There is an effective process for identifying and responding appropriately to signs and allegations of abuse. There is an effective process for preventing abuse before it occurs, and minimising the risks of further abuse once it has occurred. <br>
<br>
A child is defined as a person under the age of 18.  A vulnerable adult is any person aged 18 or over who is or may be in need of health or social care services by reason of a mental, physical or learning disability, age or illness and who is or may be vulnerable to take care of him or herself, or unable to protect him or herself against significant harm or serious exploitation. <br>
<br>
Where staff are likely to engage with a child or vulnerable adult on a one-to-one basis, the staff member is appropriately trained in issues related to child and vulnerable adult protection. <br>
<br>
The lead person for child and vulnerable adult protection is [the Practice Manager]. Every team member knows the name of the lead person for child and vulnerable adult protection.
    All suspicions and allegations of abuse will be taken seriously and responded to swiftly and appropriately. All staff have a responsibility to report concerns to the appropriate lead member of staff. All team members are required to undergo an enhanced Criminal Record Bureau Check (CRB check). The practice will not employ anyone who has been barred by the Independent Safeguarding Authority (ISA). <strong><br>
    <br>
Good practice guidelines</strong><br>
<br>
A chaperone is always present when treating a child or vulnerable adult. <br>
<br>
Gratuitous physical contact is never made with a patient. If a patient needs comforting, staff use discretion to ensure that any physical contact is appropriate.<br>
<br>
Physical force is never used against a patient, unless it constitutes reasonable restraint to protect him/her or another person or to protect property. If it is necessary to restrain a patient because they are an immediate danger to themselves or others or to property the minimum amount of force is used for the shortest amount of time. <br>
<br>
Any problems are referred to the child and vulnerable adult protection lead.  <strong><br>
