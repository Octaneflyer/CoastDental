    <strong>Prevention and Public health<br>
    <br>
    </strong> The practice is committed to maintaining an evidence-based prevention policy for infection and disease oral diseases and conditions appropriate to the needs of the local population, which is consistent with local and national priorities.  
    Clinical team members are appropriately trained to provide relevant information and guidance to patients including:
    <ul type="square">
      <li>The benefits of fluoridated toothpastes and other oral      care products and providing patients (and parents/carers of young people)      with appropriate oral hygiene instruction</li>
      <li>The promotion of healthy diets with less sugar and more      fruit and vegetables</li>
      <li>A check on smoking status and appropriate advice on      smoking reduction to lower the risk of periodontal disease and oral cancer</li>
