Our private fees are designed to be fair and to enable us to offer patients the freedom of choice to have advanced treatments. We operate a robust patient complaints procedure. All comments and suggestions are welcomed and taken very seriously because they help us to continually improve our services to patients.
    Contemporaneous records are maintained on Computer records. <strong>Patient Consent</strong> The practice follows the GDC guidelines &lsquo;Principles of Patient Consent&rsquo;. All clinical team members providing treatment requiring consent are adequately trained and ensure that the patient has:
    <ul type="square">
      <li>Enough information to make a decision (informed consent)</li>
      <li>Made a decision (voluntary decision-making)</li>
      <li>The ability to make an informed decision (ability)</li>
    </ul>
&nbsp;
    The nature of treatment (NHS or private) and all charges are clarified to the patient before it commences and s/he is provided with a written treatment plan and cost estimate. All team members are aware that once the consent has been given it may be withdrawn at any time and they will respect the patient&rsquo;s decision. If the team member is uncertain about the patient&rsquo;s ability to give informed consent they will consult their dental defence organisation for advice. <br>
No person may provide consent for treatment of another adult and all healthcare professionals, including dentists, must have regard to the Mental Capacity Act Code. There is always a legal presumption of capacity and in order to give consent a person must be able:
<ul type="square">
  <li>To understand relevant information </li>
  <li>To retain that information </li>
  <li>To use/weigh it up in decision-making process </li>
  <li>To communicate decision (speech, sign language or any      other means)</li>
</ul>
If a person is thought to lack capacity a two stage test of capacity should be applied:
<ul type="square">
  <li>Is there impaired/disturbed functioning of mind or brain      for reasons of conditions associated with some forms of mental illness?</li>
  <li>Dementia</li>
  <li>Significant learning disabilities</li>
  <li>The long-term effects of brain damage</li>
  <li>Physical or medical conditions that cause confusion,      drowsiness or</li>
</ul>
Loss of consciousness
<ul type="square">
  <li>delirium</li>
  <li>Concussion following a head injury</li>
  <li>The symptoms of alcohol or drug use.</li>
  <li>Is it sufficient to believe that the person lacks      capacity to make the particular decision?</li>
</ul>
Where there is reasonable belief or it is claimed that the person is not capable of making a decision for themselves the belief or claim must be evidenced. This should show, on the balance of probabilities, that the individual lacks capacity to make a particular decision, at the time it needs to be made. This means being able to show that it is more likely than not that the person lacks capacity to make the decision in question.  A person has the right to refuse to be assessed for mental capacity to reach a decision and may not be coerced or forced into accepting assessment.<br>
<br>
The dentist may require factual information from the GP regarding any clinical problems likely to impair mental functioning, but the dentist must himself assess the patient's capacity to make the particular decision to consent to the specific proposed treatment at that particular time.  Where the patient is in the care of another, for reasons of their incapacity to make their own reasoned decisions, the dentist must maintain the best interests of the patient at all times; where there is conflict between the professional and carer, the ICMA will provide assistance.   <br>
<br>
The dentist may make a 'best interests' decision to treat a patient who lacks capacity without obtaining prior consent, but this must always follow the <strong>Statutory Checklist</strong> and be in line with the guidance given in the Code of Practice.&nbsp; The reasons for the decision should be carefully documented for legal reasons within the patient notes.<br>
