<u>Services Offered at Coast Dental Health &amp; Beauty Ltd</u><br>
<br>
Services will be offered by the Dental Hygienist/Therapist and Dentist.<br>
<br>
The Dental Hygienist is a registered dental professional who will help patients maintain their oral health by preventing and treating gum disease and promoting good oral health practice.  They will carry out treatment under the prescription from a dentist.<br>
<br>
They will provide dental hygiene care to a wide range of patients/plan the delivery of care for patients to improve and maintain their periodontal health/obtain a detailed dental history from patients and evaluate their medical history/complete periodontal examination and charting and use indices to screen and monitor periodontal disease/provide preventive oral care to patients and liaise with dentists over the treatment of caries, periodontal disease and tooth wear/undertake supra gingival and sub gingival scaling and root debridement using manual and powered instruments/use appropriate anti-microbial therapy to manage plaque related disease/adjust restored surfaces in relation to periodontal treatment/apply topical treatments and fissure sealants/give patients advice on how to stop smoking/take, process and interpret various film views used in general dental practice/give infiltration and inferior dental block analgesia under prescription from dentist/place temporary dressings and re cement crowns with temporary cement/take impressions/identify anatomical features, recognise abnormalities and interpret common pathology, and carry out oral cancer screening/if necessary, refer patients to other health care professionals/placing rubber dam/ tooth whitening only if appropriate training has been undertaken and under the prescription of the dentist.<br>
<br>
The Dental Hygienist will not undertake any of the skills as follows<br>
<br>
The Dental Therapist is a registered dental professional who will carry out certain items of dental treatment under prescription from a dentist.  The dental therapist will cover the same areas as  the dental hygienist, but the therapists will also carry out direct restorations on permanent and primary teeth/carry out pulpotomies on primary teeth/extract primary teeth/place pre-formed crowns on primary teeth/plan the delivery of their patients care and tooth whitening if appropriate training has been undertaken and under the prescription of the dentist<br>
<br>
The therapist will not carry out a patients initial diagnosis or take overall responsibility for planning a patients treatment and they do not undertake any of the skill areas described in the rest of this statement of purpose<br>
<br>
The Dentist will carry out all of the treatments we have already mentioned as well as diagnosing disease/prepare comprehensive treatment plans (this is a strategic role as a treatment plan can be taken to any appropriate DCP – dentists are not the only dental health professionals to plan how to deliver care, but they have an overall long-term responsibility for the treatment)/prescribe and provide endodontic treatment/prescribe and provide fixed and removable prostheses/surgical treatments (eg extractions)/treat oral trauma/treat dental disease, including caries and periodontal disease/prescribe and provide crowns and bridges/prescribe &amp; administer local anaesthetic/prescribe drugs as part of dental treatment/prescribe and interpret radiographs<br>
<br>
The following treatments will be referred to outside specialists when required<br>
<br>
Complicated endodontic treatment/orthodontic treatment/oral surgery/periodontal surgery/patients who need to be treated under general anaesthesia, inhalational and intravenous conscious sedation and implants.<br>
