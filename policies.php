Data Protection</strong><br>
<br>
The practice is committed to complying with the Data Protection Act 1998 by collecting, holding, maintaining and accessing data in an open and fair fashion. <br>
<br>
The practice will only keep relevant information about employees for the purposes of employment, or about patients to provide them with safe and appropriate dental care. The practice will not process any relevant &lsquo;sensitive personal data&rsquo; without prior informed consent. As defined by the Act &lsquo;sensitive personal data&rsquo; is that related to political opinion, racial or ethnic origin, membership of a trade union, the sexual life of the individual, physical or mental health or condition, religious or other beliefs of a similar nature. Sickness and accidents records will also be kept confidential. <br>
<br>
All manual and computerised records will be kept in a secure place; they will be regularly reviewed, updated and destroyed in a confidential manner when no longer required. Personnel records will only be seen by appropriate management. <br>
<br>
Patients&rsquo; records will only be seen by appropriate team members. To facilitate patients&rsquo; health care the personal information about them may be disclosed to a doctor, health care professional, hospital, NHS authorities, the Inland Revenue, the Benefits Agency (when claiming exemption or remission from NHS charges) or private dental schemes of which the patient is a member. In all cases the information shared will be only that which is relevant to the situation. In very limited cases, such as for identification purposes, or if required by law, information may have to be shared with a party not involved in the patient&rsquo;s health care. In all other cases, information will not be disclosed to such a third party without the patient&rsquo;s written authority.
    &nbsp; <br>
    <br>
    <strong>Access to records</strong><br>
    <br>
Patients and team members can have access to the original of the records kept about them free of charge. To receive a copy of all records kept about them by the practice a team member or a patient should make a written request to the [Practice Manager] .  The [Practice Manager] will provide a copy within a period of 40 days. An employee or a patient may challenge information held on record and following investigation should the information be inaccurate; the practice will correct the information and inform the patient or the team member of the change in writing. <br>
<br>
The full Data Protection Policy can be viewed in (M 216). <strong><br>
