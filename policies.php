The importance of obtaining accurate medical history of patients cannot be stressed too highly.  It should always be ascertained whether there has been any change in medical circumstances ahead of commencing treatment, particularly where drugs may be an element of the treatment plan to be followed.  Intolerance or allergy will occasionally manifest with no previous history. <strong><br>
