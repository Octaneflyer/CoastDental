Infection Prevention and Control Policy</strong><br>
<br>
<strong>Statement of intent</strong>  <br>
<br>
For the safety of our patients, visitors and team this practice follows the latest guidelines and research on infection prevention. We comply with the &lsquo;essential quality requirements&rsquo; from the Department of Health and have a written assessment and plan to move towards &lsquo;best practice&rsquo;. We take Universal Precautions for all patients, to minimise all of the known and unknown risks of cross infection.   <strong></strong> <strong><br>
<br>
Decontamination</strong><br>
<br>
We follow the latest decontamination guidelines from the Department of Health for new and used instruments. Stored instruments are protected against recontamination. The treatment rooms and all equipment are decontaminated appropriately between patients and at the end of every clinical session. Defects found during the cleaning of equipment are immediately reported to the Decontamination Lead <br>
<br>
<strong>Immunisation</strong><br>
Staff involved in decontamination and clinical work have evidence of current immunisation for Hepatitis B <br>
<br>
<strong>Items sent to the laboratory and equipment sent for repair</strong><br>
<br>
All items despatched to the laboratory are washed and disinfected after removal from the mouth and items received from the laboratory are washed and disinfected prior to fitting. Equipment is decontaminated before being sent for repair <br>
<br>
<strong>Instruments</strong><br>
<br>
Whenever possible we utilise single-use instruments, which are always disposed of after use on a patient<br>
<br>
<strong>Inoculation injury</strong><br>
<br>
To minimise the risk of blood borne viruses all staff are trained in avoidance and management of an inoculation injury. Post Exposure Prophylaxis is available if necessary. Staff at risk of blood-borne virus exposure have an occupational health examination<strong></strong> <strong><br>
<br>
Legionella control</strong><br>
<br>
The practice takes all reasonable measures to minimise the risk of exposure of staff, patients and visitors to legionella in accordance with existing guidance. The practice carries out regular legionella risk assessment, water tests and audits. Flushing of hot and cold water outlets is routinely undertaken by the practice. Records of all legionella control activities are maintained and reviewed at the Annual Management Review <br>
