Prevention strategy in accordance with &lsquo;Delivering Better Health&rsquo;</strong> <br>
<br>
This practice is committed to oral health through prevention. Patients are regularly screened for their gum condition, oral cancer and dental disease. Oral hygiene, dietary and lifestyle advice is given as needed. All patients are assessed for risk factors and advised accordingly. The relevant information on: tooth brushing, fluoride, diet and smoking cessation as found in &lsquo;Delivering Better Oral Health&rsquo; is provided whenever it is appropriate and in the best interests of the patient. 
    The care delivered encourages the prevention and early detection of ill health and enables the person to make healthy living choices.<br>
