<strong>Complaints Handling Policy</strong><br>
<br>
At Coast Dental Health &amp; Beauty Ltd we take complaints very seriously because we want all our patients to be happy with our services.  Our reputation is very important to us and in the event of a complaint we will ensure prompt action to ensure the matter is resolved as quickly as possible.  As with all our services we want to handle your complaint in the same manner as we would want our own complaints dealt with.  <br>
<br>
We really would like to encourage our patients to give us feedback when they are unhappy as this will really help us improve the services that we provide to you.|<br>
<br>
Please be assured that any complaint will be dealt with in a confidential, caring and sensitive manner and will not have a negative effect on your treatment.  <br>
<br>
The person with overall responsibility for dealing with complaints is the Practice Owner/Manager - Jayne Ward.  However, you should not hesitate to raise any matter with any member of the practice team in the first instance.     <br>
<br>
On receipt of your complaint we will endeavour to respond in writing or by phone as soon as possible.  If we cannot sort your complaint immediately then we will endeavour to send an acknowledgement letter within 3 practice working days and never any later than 10 practice working days.  We will keep in contact with you regularly so than we can update you with the progress of our investigation.   We will keep in touch with you by phone and give reasons for any delays and the likely period within which the investigation will be completed.  The full response may initially be given at a meeting or by telephone if the patient prefers, and then confirmed in writing. <br>
<br>
If, however, we have failed to satisfy your complaint then you may wish to refer the matter to<br>
